    private void FillUsers() {
        jList1.setListData(new String[0]);
                
        DefaultListModel listModel;

        listModel = new DefaultListModel(); 
        
        Statement statement;
        dbCon.Open();
        
        try {
            
            statement = dbCon.con.createStatement();
            ResultSet result = statement.executeQuery("SELECT u.username FROM Users u INNER JOIN Notes_Users nu ON u.id = nu.user_id "
                    + "WHERE nu.note_id = " + n_id + " AND admin = 0;"); 
            
            while(result.next())
            {
                listModel.addElement(result.getString(1));
            }

            dbCon.Close();
        
        } catch (SQLException ex) {
            Logger.getLogger(loginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jList1.setModel(listModel);
    }
    
    
