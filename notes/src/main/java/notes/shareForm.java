    private void RemoveData(String username) {
        if(!username.equals(""))
        {            
            //JOptionPane.showMessageDialog(null, note_id);
            Statement statement;
            
            dbCon.Open();
            try {
                statement = dbCon.con.createStatement();
                statement.executeUpdate("DELETE FROM Notes_Users WHERE user_id = (SELECT id FROM Users WHERE username = '" + username + "');"); 

                FillUsers();
                dbCon.Close();

            } catch (SQLException ex) {
                Logger.getLogger(loginForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        FillUsers();
    }
    
